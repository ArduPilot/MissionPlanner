namespace netDxf.Units
{
    /// <summary>
    /// Default drawing units for AutoCAD DesignCenter blocks.
    /// </summary>
    public enum DrawingUnits
    {
        /// <summary>
        /// Unitless.
        /// </summary>
        Unitless = 0,

        /// <summary>
        /// Inches.
        /// </summary>
        Inches = 1,

        /// <summary>
        /// Feet.
        /// </summary>
        Feet = 2,

        /// <summary>
        /// Miles.
        /// </summary>
        Miles = 3,

        /// <summary>
        /// Millimeters.
        /// </summary>
        Millimeters = 4,

        /// <summary>
        /// Centimeters.
        /// </summary>
        Centimeters = 5,

        /// <summary>
        /// Meters.
        /// </summary>
        Meters = 6,

        /// <summary>
        /// Kilometers.
        /// </summary>
        Kilometers = 7,

        /// <summary>
        /// Microinches.
        /// </summary>
        Microinches = 8,

        /// <summary>
        /// Mils (a thousandth of an inch).
        /// </summary>
        Mils = 9,

        /// <summary>
        /// Yards.
        /// </summary>
        Yards = 10,

        /// <summary>
        /// Angstroms.
        /// </summary>
        Angstroms = 11,

        /// <summary>
        /// Nanometers.
        /// </summary>
        Nanometers = 12,

        /// <summary>
        /// Microns.
        /// </summary>
        Microns = 13,

        /// <summary>
        /// Decimeters.
        /// </summary>
        Decimeters = 14,

        /// <summary>
        /// Decameters
        /// </summary>
        Decameters = 15,

        /// <summary>
        /// Hectometers.
        /// </summary>
        Hectometers = 16,

        /// <summary>
        /// Gigameters.
        /// </summary>
        Gigameters = 17,

        /// <summary>
        /// AstronomicalUnits.
        /// </summary>
        AstronomicalUnits = 18,

        /// <summary>
        /// LightYears.
        /// </summary>
        LightYears = 19,

        /// <summary>
        /// Parsecs.
        /// </summary>
        Parsecs = 20
    }
}