                    var currentlist = GetCommandList();

                    Locationwp home = new Locationwp();
                    try
                    {
                        home.id = (ushort)MAVLink.MAV_CMD.WAYPOINT;

                        if (MainV2.comPort.MAV.cs.HomeLocation.Lat != 0 && MainV2.comPort.MAV.cs.HomeLocation.Lng != 0)
